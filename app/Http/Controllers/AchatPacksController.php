    {
        $achatpack = AcheterPack::create([
            'user_id'           => Auth::id(),
            'pack_id'           => $request['pack_id'],
            'date_achat'        => $request['date_achat'],
            'date_fin'          => $request['date_fin'],
            'statut_pack'       => 0
        ]);
        if($achatpack){
            return redirect()->route('packpubs.index');
        }
